#include<stdio.h>
    // main function
    int main()
{
//assignment operator(=)
int a,b;
a=10;
printf("value of a is %d\n",a);
     //adding value to a
     a=a+5;
     printf("value of updated a is %d\n",a);

     //initializing (assigning) value of a to b
     b=a;
     printf("value of b is %d\n",b);

     //assigment is from right to left
     int p,q;

     
     p=q=40;
     printf("value of p is %d\n",p);
     printf("value of q is %d\n",q);
return 0;
}
